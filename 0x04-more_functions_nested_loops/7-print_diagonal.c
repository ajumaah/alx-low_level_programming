#include "main.h"

/**
 * print_diagonal - draws a diagonal line
 * @n: line to be drawn
 * Return: a diagonal line
 */
void print_diagnal(int n)
{
	int x, y;

	if (n <= 0)
	{
		_putchar('\n');
	}
	else
	{
		for (x = 1; x <= n; x++)
		{
			for (y = 1; y < x; y++)
			{
				_putchar(' ');
			}
			_putchar('\\');
			_putchar('\n');
		}
	}
}
